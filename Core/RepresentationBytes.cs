namespace Core
{
    /// <summary>
    /// The number of bytes relative to the 
    /// numeric representation of the value in the memory buffer
    /// </summary>
    public enum RepresentationBytes
    {
        /// <summary>
        /// One byte
        /// </summary>
        One = 0,

        /// <summary>
        /// Two bytes
        /// </summary>
        Two = 1,

        /// <summary>
        /// Four bytes
        /// </summary>
        Four = 2,

        /// <summary>
        /// Eight bytes
        /// </summary>
        Eight = 3
    }
}
